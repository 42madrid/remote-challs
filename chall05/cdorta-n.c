int		ft_ie_except_after_c(char *str)
{
	int c;

	c = 0;
	while (str[c + 1] != '\0')
	{
		if (str[c] == 'e' && str[c + 1] == 'i')
		{
			if (str[c - 1] != 'c')
				return (0);
		}
		if (str[c] == 'i' && str[c + 1] == 'e')
		{
			if (str[c - 1] == 'c')
				return (0);
		}
		c++;
	}
	return (1);
}
